const generateInvoice = (pvz, clients) => {
  const stockId = getRandomPvz(pvz).id;
  const senderId = getRandomClient(clients).id;
  const recipientId = getRandomClient(
    clients.filter(({ id }) => id !== senderId)
  ).id;
  return {
    senderId,
    recipientId,
    stockId,
  };
};

const getRandomPvz = (pvz) => {
  const arr = Object.values(pvz).flat(Infinity);
  return arr[getRandomInt(0, arr.length)];
};

const getRandomClient = (clients) => {
  return clients[getRandomInt(0, clients.length)];
};

function getRandomInt(min, max) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min) + min); // Максимум не включается, минимум включается
}

